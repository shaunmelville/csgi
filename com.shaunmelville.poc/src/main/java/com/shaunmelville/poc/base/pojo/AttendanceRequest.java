package com.shaunmelville.poc.base.pojo;

public class AttendanceRequest {
	
	private String studentGuid;
	private String classSchedulerGuid;
	
	public String getStudentGuid() {
		return studentGuid;
	}
	public void setStudentGuid(String studentGuid) {
		this.studentGuid = studentGuid;
	}
	public String getClassSchedulerGuid() {
		return classSchedulerGuid;
	}
	public void setClassSchedulerGuid(String classSchedulerGuid) {
		this.classSchedulerGuid = classSchedulerGuid;
	}
	
	

}
